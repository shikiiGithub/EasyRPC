package com.JavaHost.Helpers;

public class LogItemInfo
{
    /// <summary>
    /// 服务名
    /// </summary>
    public String ServiceName  ;
    /// <summary>
    /// 触发的日期
    /// </summary>
    public String FireDate ;

    /// <summary>
    /// 触发时间（时分秒）
    /// </summary>
    public String FireTime ;

    /// <summary>
    /// 触发等级 DEBUG INFO  WARN ERROR
    /// </summary>
    public String  Level ;

    /// <summary>
    /// Id 可选
    /// </summary>
    public String Id ;

    /// <summary>
    /// 日志内容
    /// </summary>
    public String Content ;

}
